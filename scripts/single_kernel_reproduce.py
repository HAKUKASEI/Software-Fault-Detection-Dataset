@njit(cache=True, fastmath=True)
def _predict_point_loo(X, Y, x_test, h, kernel_id, estimator_id, leave_idx):
    if estimator_id == 0:
        return _nw_predict_point_loo(X, Y, x_test, h, kernel_id, leave_idx)
    return _ll_predict_point_loo(X, Y, x_test, h, kernel_id, leave_idx)


@njit(cache=True, fastmath=True)
def _calculate_lscv_integral_numba(X, Y, h, kernel_id, estimator_id, num_grid):
    n = X.shape[0]
    if n == 0 or num_grid < 2:
        return 1e10

    xmin = X[0]
    xmax = X[0]
    for i in range(1, n):
        if X[i] < xmin:
            xmin = X[i]
        if X[i] > xmax:
            xmax = X[i]

    if xmin == xmax:
        padding = 0.1
    else:
        padding = (xmax - xmin) * 0.1

    a = xmin - padding
    b = xmax + padding
    dx = (b - a) / (num_grid - 1)

    total = 0.0
    for g in range(num_grid):
        xg = a + dx * g
        y = _predict_point(X, Y, xg, h, kernel_id, estimator_id)
        val = y * y
        if val > 1e10:
            val = 1e10
        if math.isnan(val) or math.isinf(val):
            return 1e10

        if g == 0 or g == num_grid - 1:
            total += 0.5 * val
        else:
            total += val

    return total * dx


@njit(cache=True, fastmath=True)
def _lscv_cross_term_numba(X, Y, h, kernel_id, estimator_id):
    r"""
    Cross term corresponding to the formula in the manuscript/image:

        sum_i { (1/n) * sum_j \hat{Lambda}_{(-j),h}(x_i) } * \hat{Lambda}_h(x_i)

    where:
    - \hat{Lambda}_h(x_i) is the estimator using all observations;
    - \hat{Lambda}_{(-j),h}(x_i) is the estimator with the j-th observation removed.

    Other experimental settings are unchanged.
    """
    n = X.shape[0]
    if n == 0:
        return 1e10

    total = 0.0

    for i in range(n):
        xi = X[i]

        # \hat{Lambda}_h(x_i): full-data estimator
        full_pred = _predict_point(X, Y, xi, h, kernel_id, estimator_id)

        # (1/n) * sum_j \hat{Lambda}_{(-j),h}(x_i)
        loo_avg = 0.0
        for j in range(n):
            loo_pred = _predict_point_loo(X, Y, xi, h, kernel_id, estimator_id, j)
            loo_avg += loo_pred
        loo_avg = loo_avg / n

        total += loo_avg * full_pred

    return total


@njit(cache=True, fastmath=True)
def _lscv_score_numba(X, Y, h, kernel_id, estimator_id, num_grid):
    integral_term = _calculate_lscv_integral_numba(X, Y, h, kernel_id, estimator_id, num_grid)
    cross_term = _lscv_cross_term_numba(X, Y, h, kernel_id, estimator_id)

    if integral_term >= 1e9 or abs(cross_term) >= 1e9:
        return 1e10

    score = integral_term - 2.0 * cross_term
    if math.isnan(score) or math.isinf(score):
        return 1e10
    return score


@njit(cache=True, fastmath=True)
def _find_best_h_numba(X, Y, h_candidates, kernel_id, estimator_id, lscv_grid_size):
    best_h = -1.0
    best_score = 1e100

    for i in range(h_candidates.shape[0]):
        h = h_candidates[i]
        score = _lscv_score_numba(X, Y, h, kernel_id, estimator_id, lscv_grid_size)
        if score < best_score:
            best_score = score
            best_h = h

    return best_h, best_score


@njit(cache=True, fastmath=True)
def _predict_next_numba(X, Y, h, kernel_id, estimator_id):
    i = X.shape[0]
    pred_1 = _predict_point(X, Y, 1.0, h, kernel_id, estimator_id)
    pred_prev = _predict_point(X, Y, i / (i + 1.0), h, kernel_id, estimator_id)
    return Y[i - 1] + (pred_1 - pred_prev)


# ====== 5. Python estimator interface (retained; main experiment uses Numba) ======
def NW_estimator(X_train, y_train, x_test, h_list, w_list, kernel_func):
    X_train = np.asarray(X_train).reshape(-1, 1)
    y_train = np.asarray(y_train).reshape(-1, 1)
    x_test_array = np.atleast_1d(x_test).reshape(1, -1)

    t_diffs = x_test_array - X_train
    k_vals = kernel_func(t_diffs, h_list, w_list)

    numerator = np.sum(k_vals * y_train, axis=0)
    denominator = np.sum(k_vals, axis=0)

    with np.errstate(divide='ignore', invalid='ignore'):
        y_pred = numerator / denominator
    y_pred = np.nan_to_num(y_pred, nan=0.0)

    if np.isscalar(x_test):
        return y_pred.item()
    return y_pred


def LL_estimator(X_train, y_train, x_test, h_list, w_list, composite_kernel_func_arg):
    X_train = np.asarray(X_train).reshape(-1, 1)
    y_train = np.asarray(y_train).reshape(-1, 1)
    x_test_array = np.atleast_1d(x_test).reshape(1, -1)

    t_diffs = x_test_array - X_train
    k_vals = composite_kernel_func_arg(t_diffs, h_list, w_list)

    S0 = np.sum(k_vals, axis=0)
    S1 = np.sum(k_vals * t_diffs, axis=0)
    S2 = np.sum(k_vals * (t_diffs ** 2), axis=0)

    w_contribs = k_vals * (S2 - t_diffs * S1)

    numerator = np.sum(w_contribs * y_train, axis=0)
    denominator = np.sum(w_contribs, axis=0)

    with np.errstate(divide='ignore', invalid='ignore'):
        y_pred = numerator / denominator
    y_pred = np.nan_to_num(y_pred, nan=0.0)

    if np.isscalar(x_test):
        return y_pred.item()
    return y_pred


# ====== 6. Single-kernel bandwidth selection: Numba-accelerated grid search ======
def bandwidth_grid(n_train, num_grid=100):
    """
    Generate candidate bandwidth values for the single-kernel methods.

    h_min = min(0.95, max(0.05, 3 / n_train))
    h_max = 1.0
    num_grid = 100
    """
    h_max = 1.0
    h0 = 0.05
    k_neighbors = 3
    eps = 0.05

    if n_train <= 0:
        raise ValueError("n_train must be positive.")

    h_min = min(h_max - eps, max(h0, k_neighbors / n_train))
    h_values = np.linspace(h_min, h_max, num_grid).astype(np.float64)
    return h_values, h_min, h_max


def find_optimal_bandwidth_grid_numba(X, Y, kernel_id, estimator_id, num_grid=100, lscv_grid_size=100):
    """
    Deterministic Numba-accelerated grid search for the single-kernel methods.
    The weight is fixed at w=1.0; no PSO and no h=1.0 fallback are used.
    """
    X = np.asarray(X, dtype=np.float64)
    Y = np.asarray(Y, dtype=np.float64)
    h_candidates, h_min, h_max = bandwidth_grid(len(X), num_grid=num_grid)

    best_h, best_score = _find_best_h_numba(X, Y, h_candidates, kernel_id, estimator_id, lscv_grid_size)

    if best_h < 0.0 or not np.isfinite(best_score):
        raise RuntimeError("No valid bandwidth found in Numba grid search.")

    return [float(best_h)], [1.0], float(best_score), float(h_min), float(h_max), int(num_grid)


def normalized_x(n):
    return (np.arange(1, n + 1, dtype=np.float64) / float(n))


def load_datasets_from_excel_folder(data_folder_name="data"):
    """
    Load every Excel dataset from data_folder_name.

    Accepted formats:
    - one numeric column: y values; an implicit x index 1..n is used;
    - two numeric columns: x and y; x is used only for sorting.

    The estimator itself uses normalized_x(n), matching the original experiment.
    """
    data_dir = os.path.abspath(data_folder_name)

    if not os.path.isdir(data_dir):
        raise FileNotFoundError(f"Data directory not found: {data_dir}")

    excel_files = []
    for fname in os.listdir(data_dir):
        lower = fname.lower()
        if lower.startswith("~$"):
            continue
        if lower.endswith((".xlsx", ".xlsm", ".xls")):
            excel_files.append(fname)

    excel_files = sorted(excel_files)

    if len(excel_files) == 0:
        raise FileNotFoundError(f"No Excel files found in: {data_dir}")

    datasets = {}

    for fname in excel_files:
        file_path = os.path.join(data_dir, fname)
        dataset_name = os.path.splitext(fname)[0]

        try:
            df = pd.read_excel(file_path, header=None)
        except Exception as e:
            raise RuntimeError(f"Failed to read Excel file: {file_path}, error={repr(e)}")

        df = df.dropna(how="all")
        non_empty_cols = [col for col in df.columns if not df[col].dropna().empty]

        if len(non_empty_cols) == 0:
            raise ValueError(f"{fname} has no non-empty columns.")

        if len(non_empty_cols) == 1:
            y_col = non_empty_cols[0]
            y_values = pd.to_numeric(df[y_col], errors="coerce").dropna().astype(float).to_numpy()
        else:
            x_col, y_col = non_empty_cols[:2]
            work = df[[x_col, y_col]].copy()
            work[x_col] = pd.to_numeric(work[x_col], errors="coerce")
            work[y_col] = pd.to_numeric(work[y_col], errors="coerce")
            work = work.dropna(subset=[x_col, y_col])
            work = work.sort_values(by=x_col).reset_index(drop=True)
            y_values = work[y_col].astype(float).to_numpy()

        if len(df) < 3:
            raise ValueError(f"{fname} has too few raw rows; at least 3 are required.")
        if len(y_values) < 3:
            raise ValueError(f"{fname} has too few valid numeric points; at least 3 are required.")

        datasets[dataset_name] = y_values.tolist()
        print(f"Loaded dataset: {dataset_name}, points={len(y_values)}, file={fname}")

    print(f"\nLoaded {len(datasets)} Excel dataset(s).")
    return datasets


# ====== 7. Worker task helpers (single-kernel Numba grid search) ======
def select_best_kernel_by_observation_lscv(base_X, base_Y, estimator_id, grid_size=100, lscv_grid_size=100):
    """
    Compare observation-stage LSCV across the four single kernels.
    Each kernel first finds its own best h by grid search; the smallest LSCV is
    then used for kernel selection.
    """
    best = None

    for kernel_name in SINGLE_KERNEL_NAMES:
        kernel_id = KERNEL_ID_MAP[kernel_name]
        try:
            opt_h, opt_w, opt_score, _, _, _ = find_optimal_bandwidth_grid_numba(
                base_X, base_Y, kernel_id, estimator_id, num_grid=grid_size, lscv_grid_size=lscv_grid_size
            )
        except Exception:
            # If one kernel fails, keep the case running and mark that kernel unavailable.
            opt_h = [np.nan]
            opt_w = [1.0]
            opt_score = 1e100

        if best is None or opt_score < best["score"]:
            best = {
                "kernel_name": kernel_name,
                "kernel_id": kernel_id,
                "h": opt_h,
                "w": opt_w,
                "score": float(opt_score),
            }

    if best is None or not np.isfinite(best["score"]) or best["score"] >= 1e99:
        raise RuntimeError("No valid kernel/bandwidth found by observation-stage LSCV.")

    return best


# ====== 8. Worker task: select the best observation-stage single kernel per case ======
def process_single_stage(args):
    (dataset_name, DS_original, estimator_name, estimator_fn, start_idx,
     target_ratio, grid_size, lscv_grid_size) = args

    estimator_id = ESTIMATOR_ID_MAP[estimator_name]

    DS_arr = np.asarray(DS_original, dtype=np.float64)
    number = len(DS_arr)
    predictionLimit = number

    base_Y = DS_arr[:start_idx].copy()
    base_X = normalized_x(start_idx)

    # Select the kernel with the smallest observation-stage LSCV.
    best_kernel = select_best_kernel_by_observation_lscv(
        base_X, base_Y, estimator_id, grid_size=grid_size, lscv_grid_size=lscv_grid_size
    )

    kernel_id = best_kernel["kernel_id"]
    opt_h = best_kernel["h"]

    actual_vals = DS_arr[start_idx:predictionLimit]

    # --- Fixed Loop ---
    # Fixed mode: keep the observation-stage kernel and h fixed.
    curr_Y_fixed = np.empty(predictionLimit, dtype=np.float64)
    curr_Y_fixed[:start_idx] = base_Y
    preds_fixed = np.empty(predictionLimit - start_idx, dtype=np.float64)

    i = start_idx
    while i < predictionLimit:
        curr_X = normalized_x(i)
        pred_next = _predict_next_numba(curr_X, curr_Y_fixed[:i], opt_h[0], kernel_id, estimator_id)
        curr_Y_fixed[i] = pred_next
        preds_fixed[i - start_idx] = pred_next
        i += 1

    res_fixed = {
        "Dataset": dataset_name,
        "Estimator": estimator_name,
        "Bandwidth_Mode": "Fixed",
        "Target_Start_Ratio": float(target_ratio),
        "Actual_Start_Ratio": start_idx / number,
        "PMAE": mean_absolute_error(actual_vals, preds_fixed),
        "Predicted_Y_Sequence": ",".join(map(str, preds_fixed.tolist())),
    }

    # --- Dynamic Loop ---
    # Dynamic mode: keep the selected kernel fixed and reselect h at each step.
    curr_Y_dyn = np.empty(predictionLimit, dtype=np.float64)
    curr_Y_dyn[:start_idx] = base_Y
    preds_dyn = np.empty(predictionLimit - start_idx, dtype=np.float64)

    h_dyn = opt_h

    i = start_idx
    first = True
    while i < predictionLimit:
        curr_X = normalized_x(i)
        if first:
            first = False
        else:
            h_dyn, _, _, _, _, _ = find_optimal_bandwidth_grid_numba(
                curr_X, curr_Y_dyn[:i], kernel_id, estimator_id, num_grid=grid_size, lscv_grid_size=lscv_grid_size
            )

        pred_next = _predict_next_numba(curr_X, curr_Y_dyn[:i], h_dyn[0], kernel_id, estimator_id)
        curr_Y_dyn[i] = pred_next
        preds_dyn[i - start_idx] = pred_next
        i += 1

    res_dyn = {
        "Dataset": dataset_name,
        "Estimator": estimator_name,
        "Bandwidth_Mode": "Dynamic",
        "Target_Start_Ratio": float(target_ratio),
        "Actual_Start_Ratio": start_idx / number,
        "PMAE": mean_absolute_error(actual_vals, preds_dyn),
        "Predicted_Y_Sequence": ",".join(map(str, preds_dyn.tolist())),
    }

    return [res_fixed, res_dyn]


