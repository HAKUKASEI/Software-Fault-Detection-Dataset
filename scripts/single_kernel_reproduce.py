@njit(cache=True, fastmath=True)
def _kernel_value(diff, h, kernel_id):
    if h <= 0.0:
        return 0.0

    x = diff / h
    ax = abs(x)

    # 0: Gaussian
    if kernel_id == 0:
        return (1.0 / h) * (1.0 / math.sqrt(2.0 * math.pi)) * math.exp(-0.5 * x * x)

    # 1: Triangular
    if kernel_id == 1:
        if ax <= 1.0:
            return (1.0 - ax) / h
        return 0.0

    # 2: Biweight
    if kernel_id == 2:
        if ax <= 1.0:
            tmp = 1.0 - x * x
            return (15.0 / 16.0) * (tmp * tmp) / h
        return 0.0

    # 3: Epanechnikov
    if kernel_id == 3:
        if ax <= 1.0:
            return 0.75 * (1.0 - x * x) / h
        return 0.0

    return 0.0


@njit(cache=True, fastmath=True)
def _nw_predict_point(X, Y, x_test, h, kernel_id):
    numerator = 0.0
    denominator = 0.0

    for i in range(X.shape[0]):
        k = _kernel_value(x_test - X[i], h, kernel_id)
        numerator += k * Y[i]
        denominator += k

    if denominator == 0.0 or math.isnan(denominator) or math.isinf(denominator):
        return 0.0

    y = numerator / denominator
    if math.isnan(y) or math.isinf(y):
        return 0.0
    return y


@njit(cache=True, fastmath=True)
def _ll_predict_point(X, Y, x_test, h, kernel_id):
    n = X.shape[0]
    S1 = 0.0
    S2 = 0.0

    # First pass: compute S1 and S2.
    for i in range(n):
        d = x_test - X[i]
        k = _kernel_value(d, h, kernel_id)
        S1 += k * d
        S2 += k * d * d

    numerator = 0.0
    denominator = 0.0

    # Second pass: compute local-linear weight contributions.
    for i in range(n):
        d = x_test - X[i]
        k = _kernel_value(d, h, kernel_id)
        wc = k * (S2 - d * S1)
        numerator += wc * Y[i]
        denominator += wc

    if denominator == 0.0 or math.isnan(denominator) or math.isinf(denominator):
        return 0.0

    y = numerator / denominator
    if math.isnan(y) or math.isinf(y):
        return 0.0
    return y


@njit(cache=True, fastmath=True)
def _predict_point(X, Y, x_test, h, kernel_id, estimator_id):
    if estimator_id == 0:
        return _nw_predict_point(X, Y, x_test, h, kernel_id)
    return _ll_predict_point(X, Y, x_test, h, kernel_id)


@njit(cache=True, fastmath=True)
def _nw_predict_point_loo(X, Y, x_test, h, kernel_id, leave_idx):
    numerator = 0.0
    denominator = 0.0

    for i in range(X.shape[0]):
        if i == leave_idx:
            continue
        k = _kernel_value(x_test - X[i], h, kernel_id)
        numerator += k * Y[i]
        denominator += k

    if denominator == 0.0 or math.isnan(denominator) or math.isinf(denominator):
        return 0.0

    y = numerator / denominator
    if math.isnan(y) or math.isinf(y):
        return 0.0
    return y


