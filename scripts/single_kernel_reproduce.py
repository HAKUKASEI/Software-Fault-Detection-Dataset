def warm_up_numba():
    try:
        warm_X = np.array([0.5, 1.0], dtype=np.float64)
        warm_Y = np.array([1.0, 2.0], dtype=np.float64)
        warm_h = np.array([0.95, 1.0], dtype=np.float64)
        for warm_kernel_id in range(4):
            _find_best_h_numba(warm_X, warm_Y, warm_h, warm_kernel_id, 0, 10)
            _find_best_h_numba(warm_X, warm_Y, warm_h, warm_kernel_id, 1, 10)
    except Exception:
        pass


def build_tasks(datasets, estimators_dict, start_ratios, grid_size, lscv_grid_size):
    tasks = []
    for estimator_name, estimator_fn in estimators_dict.items():
        for dataset_name, dataset_values in datasets.items():
            number = len(dataset_values)
            seen_start_indices = set()
            for ratio in start_ratios:
                start_idx = math.ceil(number * ratio)
                if start_idx in seen_start_indices:
                    continue
                seen_start_indices.add(start_idx)
                if 2 <= start_idx < number:
                    tasks.append((
                        dataset_name,
                        dataset_values,
                        estimator_name,
                        estimator_fn,
                        start_idx,
                        ratio,
                        grid_size,
                        lscv_grid_size,
                    ))
                else:
                    print(
                        f"Skipping {dataset_name} at ratio {ratio}: "
                        f"start index {start_idx} is invalid for n={number}."
                    )
    return tasks


def run_tasks(tasks, processes):
    results = []
    if processes <= 1:
        for i, task in enumerate(tasks, start=1):
            results.extend(process_single_stage(task))
            if i % 10 == 0 or i == len(tasks):
                print(f"Progress: {i}/{len(tasks)} cases completed")
        return results

    with Pool(processes=processes) as pool:
        for i, result_list in enumerate(pool.imap_unordered(process_single_stage, tasks), start=1):
            results.extend(result_list)
            if i % 10 == 0 or i == len(tasks):
                print(f"Progress: {i}/{len(tasks)} cases completed")
    return results


def write_compact_workbook(results, output_path):
    sheet_map = {
        ("NW_estimator", "Fixed"): "NW_Fixed",
        ("NW_estimator", "Dynamic"): "NW_Dynamic",
        ("LL_estimator", "Fixed"): "LL_Fixed",
        ("LL_estimator", "Dynamic"): "LL_Dynamic",
    }

    with pd.ExcelWriter(output_path, engine="openpyxl") as writer:
        for (estimator, mode), sheet_name in sheet_map.items():
            rows = [
                {col: result[col] for col in OUTPUT_COLUMNS}
                for result in results
                if result["Estimator"] == estimator and result["Bandwidth_Mode"] == mode
            ]
            df = pd.DataFrame(rows, columns=OUTPUT_COLUMNS)
            if not df.empty:
                df = df.sort_values(["Dataset", "Target_Start_Ratio"]).reset_index(drop=True)
            df.to_excel(writer, sheet_name=sheet_name, index=False)


def main():
    args = parse_args()
    if args.processes < 1:
        raise ValueError("--processes must be at least 1")
    if args.grid_size < 2:
        raise ValueError("--grid-size must be at least 2")
    if args.lscv_grid_size < 2:
        raise ValueError("--lscv-grid-size must be at least 2")

    datasets = load_datasets_from_excel_folder(args.data_dir)
    estimators_to_run = {
        "NW_estimator": NW_estimator,
        "LL_estimator": LL_estimator,
    }

    output_path = args.output
    if output_path is None:
        timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = f"SingleKernel_Repro_{timestamp}.xlsx"
    output_path = os.path.abspath(output_path)

    tasks = build_tasks(
        datasets,
        estimators_to_run,
        args.start_ratios,
        args.grid_size,
        args.lscv_grid_size,
    )
    if not tasks:
        raise RuntimeError("No valid prediction cases were generated.")

    print(f"Working directory: {os.getcwd()}")
    print(f"Output file: {output_path}")
    print(f"Cases: {len(tasks)}")
    print(f"Processes: {args.processes}")

    warm_up_numba()
    results = run_tasks(tasks, args.processes)
    write_compact_workbook(results, output_path)

    print(f"\nDone. Results saved to: {output_path}")


if __name__ == "__main__":
    freeze_support()
    main()
